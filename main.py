            bot_task = asyncio.create_task(
                self._run_with_error_handling(bot_main, "–û—Å–Ω–æ–≤–Ω–æ–π –±–æ—Ç")
            )
            admin_bot_task = asyncio.create_task(
                self._run_with_error_handling(admin_bot_main, "–ê–¥–º–∏–Ω-–±–æ—Ç")
            )
            
            self.tasks = [bot_task, admin_bot_task]
            
            logger.info("‚úÖ –û—Å–Ω–æ–≤–Ω–æ–π –±–æ—Ç –∑–∞–ø—É—â–µ–Ω")
            logger.info("‚úÖ –ê–¥–º–∏–Ω-–±–æ—Ç –∑–∞–ø—É—â–µ–Ω")
            logger.info("üéâ –°–∏—Å—Ç–µ–º–∞ –ß–∏–ª–ª–∏–í–∏–ª–∏ –ø–æ–ª–Ω–æ—Å—Ç—å—é –≥–æ—Ç–æ–≤–∞ –∫ —Ä–∞–±–æ—Ç–µ!")
            
            # –ñ–¥–µ–º –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è –≤—Å–µ—Ö –∑–∞–¥–∞—á
